package com.defenestrate.chukkars.android.util;

public class PlayerSignupData {
	public String mPlayerId;
	public String mDate;
	public String mName;
	public String mNumChukkars;


	public PlayerSignupData(String playerId, String date, String name, String numChukkars) {
		mPlayerId = playerId;
		mDate = date;
		mName = name;
		mNumChukkars = numChukkars;
	}
}
